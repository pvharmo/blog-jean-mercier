        hermeneutics, had criticized in his long and distinguished career the
        tendency to see art as only a question of aesthetics. Art is indeed,
        according to Gadamer, also a source of knowledge about society, more
        specifically about the contemporary human experience. As artist, the
        filmmaker himself does not always plan to present this knowledge
        deliberately, as we suggested earlier. That is not a problem, quite the
