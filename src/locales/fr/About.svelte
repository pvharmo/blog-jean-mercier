<h2>LE MOVIE SHRINK</h2>
<h3 style="margin-left: 10px;">
  <em>- Comprendre nos films, comprendre notre époque</em>
</h3>
<p>
  <em>Le Movie Shrink</em> est un site voué à l’analyse de films qui ont quelque
  chose de particulièrement significatif à nous dire à propos de notre époque et
  de nos sociétés.
</p>
<p>
  En se concentrant sur une cinquantaine de films par année, à raison de quatre
  par mois, le but du site est de révéler ce que des films significatifs et
  denses nous apprennent sur la condition contemporaine, à travers des analyses
  et interprétations informées.
</p>
<p>
  Pour ce faire, le site emploie une approche qui se situe dans la tradition de
  l’
  <a href="/fr/hermeneutics">herméneutique</a>. De manière générale,
  l’herméneutique concerne l’art et les méthodes de l’interprétation. Si elle a
  bien entendu de nombreuses applications et dimensions, sa pertinence pour les
  films fait partie de la tradition qui veut l’appliquer à toute forme de
  créations artistiques. Car aux yeux de l’herméneutique, l’art n’est pas que
  question d’esthétique, mais participe à notre quête de connaissance. Il
  s’agira plus précisément ici de notre quête de connaissance au sujet de la
  société, dans ses multiples dimensions contemporaines.
</p>

<h3>Pour en savoir plus</h3>
<p>
  À la suite du visionnement de certains films, nous nous retrouvons souvent
  avec de nombreuses questions et le sentiment que la matière du film excédait
  son scénario : y avait-il, par exemple, une leçon à retenir ? Que tentait de
  nous dire le réalisateur du film ? Est-ce que le film dépassait les intentions
  du réalisateur ? Ou bien, encore, ne se trouvait-il vraiment aucune leçon à
  tirer de ce film ?
</p>
<p>
  Parfois, nous souhaiterions partager ces interrogations et notre propre
  compréhension du film avec celles des autres. Hélas, plus souvent
  qu’autrement, nos amis, notre famille ou nos collègues n’ont pas visionné le
  film en question, tandis qu’ils ont vu d’autres films que nous, de notre côté,
  n’avons pas vus. Les critiques de films jugeront souvent les mérites
  artistiques du film et lui donneront un classement comparatif, allant parfois
  jusqu’à le recommander, mais ceci ne nous éclaire pas sur le sens du film. Dès
  lors, avec qui pourrions-nous partager nos impressions, notre compréhension ?
</p>
<p>
  <em>Le Movie Shrink</em> constitue une réponse partielle, mais spécifique, à ces
  questions. Le site se veut un lieu de rassemblement, un forum initiant une conversation
  et un dialogue visant la compréhension des films particulièrement significatifs
  et denses de notre époque, réalisés sur différents continents.
</p>
<p>
  Tous nos dialogues et conversations doivent commencer par un énoncé spécifique
  qui en jette les bases. <em>Le Movie Shrink</em> débute ainsi, sur ce site, par
  un énoncé d’ouverture à propos d’un film particulier, prenant la forme d’éléments
  de compréhension, formant une interprétation du film sous examen. C’est alors cette
  première interprétation qui amorce le début des échanges.
</p>
<p>
  Une fois qu’une interprétation a été offerte sur le site, les lecteurs peuvent
  joindre la conversation sur <b>dans les commentaires de chaque article</b>, afin d’y lire les
  commentaires d’autres individus ayant vu le film en question et,
  éventuellement, afin d’y participer en offrant leurs propres compréhensions
  et interprétations. Bien entendu, les spectateurs souhaitant découvrir le film
  à neuf, sans être influencés par les compréhensions et interprétations
  offertes sur le site, pourront vouloir lire les commentaires uniquement après
  avoir visionné le film. Dans nos capsules analysant des films spécifiques se
  trouvent, à cet effet, des alertes aux « divulgâcheurs » (<em
    >spoiler alert</em
  >)
</p>
<p>
  Or, afin d’ouvrir les échanges sur l’interprétation d’un film spécifique, tel
  que suggéré précédemment, nous avons besoin de lignes directrices qui, bien
  qu’explicites et méthodiques, sont suffisamment flexibles pour s’adapter à
  l’interprétation d’une multitude de films différents. À l’aide de lignes
  directrices, nous tentons d’éviter le risque que notre analyse, interprétation
  et compréhension ne deviennent que de vagues spéculations ou ne représentent
  qu’une vision idiosyncrasique du film visionné.
</p>
<p>
  Il est alors tout naturel de se poser la question suivante : existe-t-il une
  discipline, en quelque sorte une tradition, aidant à naviguer vers une
  meilleure compréhension des films à l’étude ? Comme suggéré dès le départ,
  nous croyons avoir trouvé une telle discipline, explicite, mais flexible, dans
  l’<a href="/fr/hermeneutics">herméneutique</a>.
</p>
<p>
  <em>Le Movie Shrink</em> propose dès lors de débuter les échanges en employant
  l’approche herméneutique, appliquée aux films, dans le but d’atteindre une
  meilleure compréhension de ce qui pourrait être considéré comme l’histoire
  plus large derrière l’histoire spécifique — et plus limitée — du film. Il est
  possible que nous référions occasionnellement à cette histoire plus large
  comme étant « l’éléphant derrière l’écran ». Il est possible d’en savoir plus
  au sujet de l’herméneutique en cliquant sur le lien suivant :
  <a href="/fr/hermeneutics">herméneutique</a>.
</p>
<p>
  La présomption sous-jacente de ce site, telle qu’affirmée plus tôt, est que
  l’art, et les films en particulier, en disent beaucoup à propos d’où nous nous
  situons aujourd’hui dans l’histoire humaine. Ils peuvent ainsi être
  considérés, en effet, comme des chapitres de l’histoire humaine.
</p>
<p>
  L'<a href="/fr/hermeneutics">herméneutique</a> est impliquée dans la recherche de
  compréhension lorsqu’une œuvre d’art (dans notre cas, un film) nous dit quelque
  chose à propos d’où nous nous situons aujourd’hui, collectivement, dans l’histoire
  humaine, mais le dit d’une manière indirecte et allusive.
</p>
<p>
  L’objet de ce site est de suivre la voie herméneutique pour comprendre des
  films qui présentent des éléments intrigants et difficiles, qui demandent une
  interprétation si l’on souhaite en atteindre une pleine compréhension. Pour
  une illustration visuelle de cette idée, l’on peut considérer le tableau de
  Rembrandt de 1653 <em>Aristote contemplant un buste d’Homère</em>. L’on y voit
  bien que si le poète (Homère) y raconte une histoire (dans notre cas le
  réalisateur du film), le philosophe (Aristote ou l’analyste du film) en offre
  une compréhension qui est à même de dépasser l’histoire qui est racontée et
  les intentions de son créateur.
</p>
<p>
  Ce faisant, il faut bien comprendre que ce site n’est pas à propos de
  l’esthétique des films ou de leurs mérites artistiques. À nouveau, en prenant
  pour exemple et inspiration la peinture de Rembrandt (que l’on peut voir sur
  ce site), on ne s’attend pas à ce qu’Aristote commente de manière critique le
  style d’écriture d’Homère. Dans le même esprit, jamais ne commenterons-nous
  les aspects créatifs ou artistiques d’un film (sa direction photo ou le jeu
  des acteurs, par exemple). C’est pourquoi ce site n’est définitivement pas
  voué à la critique de films.
</p>

