<h3>Ce que ce site n'est pas</h3>
<p>
  Avant d’avancer plus en détail sur l’objet précis de ce site, il importe de
  préciser ce sur quoi il ne porte pas.
</p>
<p>
  Premièrement, bien que le titre du site puisse laisser croire que nous sommes
  à la recherche de la psychologie de l’artiste-réalisateur, <em
    >le Movie Shrink</em
  >
  n’est pas du tout à propos de la psychologie intime des artistes, mais bien à propos
  des éléments collectifs et sociaux présents dans certains films significatifs spécifiques.
  Le site ne porte donc pas, en conséquence, sur la vie intérieure ou les intentions
  des auteurs, producteurs et réalisateurs. En effet, ne sera présente ici aucune
  recherche sur les intentions de l’artiste, de l’écrivain ou du créateur. Nous sommes
  intéressés par le résultat de leur travail, à savoir le film lui-même, et croyons
  que ce dernier présente un sens qui dépasse l’intention de ses créateurs, puisque
  les artistes ne sont pas nécessairement pleinement conscients de la richesse de
  leur création. Cela dit, nous sommes certes intéressés par l’opinion qu’entretiennent
  les artistes et producteurs quant à leurs créations, mais nous n’y sommes pas liés.
  Dans cet esprit, reportons-nous à nouveau au tableau de Rembrandt de 1653,
  <em>Aristote contemplant un buste d’Homère</em>, où Aristote impose ses mains
  sur la tête d’Homère, suggérant ainsi qu’il interprétera lui-même
  l’interprétation homérienne de l’histoire grecque, lui conférant de la sorte
  un sens enrichi.
</p>
<p>
  Aristote, selon la perspective adoptée ici, apparaît intéressé par-dessus tout
  à faire la lumière sur l’histoire racontée par Homère, et non pas tant à
  dévoiler la psychologie intérieure d’Homère. L’analyste-interprète d’un film
  joue alors, devant le film à l’étude, le rôle d’Aristote : il tente de
  comprendre plus pleinement ce qu’Homère — l’artiste créateur — exprime dans
  son œuvre. C’est que, selon la thèse centrale suivie ici, le créateur ultime
  d’une œuvre d’art, ou d’une histoire, peut être considéré comme la société
  elle-même. L’œuvre créée a ainsi besoin d’être déchiffrée, par-delà le travail
  de l’artiste créateur.
</p>
<p>
  Deuxièmement, ce site n’est pas, tel qu’expliqué ailleurs, à propos des
  dimensions esthétiques des films. À aucune occasion ne commenterons-nous sur
  les forces et faiblesses esthétiques du film, qu’il s’agisse de la qualité de
  sa direction photo ou du jeu des acteurs. Plusieurs sites existent déjà à
  propos de ces dimensions : elles ne constitueront pas une dimension importante
  ici. Nous reconnaissons bien entendu l’existence de liens significatifs entre
  le style et le contenu d’un film, mais ces liens, encore une fois, ne
  constitueront pas un aspect exploré ici.
</p>
<p>
  Troisièmement, nous ne tentons pas ici d’analyser des films par le biais d’une
  forme de procédure explicite, préétablie et formelle telle que la sémiotique
  ou une méthode similaire. L'<a href="/fr/hermeneutics">herméneutique</a> ,
  essentiellement un guide général et une inspiration, n’est pas une technique
  détaillée. Dans nos vies quotidiennes, nous sommes déjà en quelque sorte des
  interprètes : nous interprétons à tout instant, à la recherche du sens des
  choses et événements qui nous entourent. C’est-à-dire que nous sommes tous
  déjà un peu interprètes ou herméneutes.
  <em>Le Movie Shrink</em> ne fait alors qu’appliquer systématiquement cette activité
  naturelle et normale dans le but de mieux comprendre des films particulièrement
  significatifs et denses.
</p>
<p>
  Par ailleurs, nous ne tenterons pas d’arrimer nos interprétations à une vision
  militante ou politiquement chargée, consciente ou non, tentant d’interpréter
  un film par le biais de la critique du capitalisme ou de la globalisation, par
  exemple. De telles approches partisanes ne feront pas partie de nos
  commentaires, du moins pas consciemment, bien qu’il soit possible d’aborder
  ces sujets sensibles dans ce que nous croyons être une approche analytique et
  réflexive. D’autres forums existent pour discuter d’analyses ouvertement
  politiques de la production cinématographique, et plusieurs d’entre eux sont
  fort éclairants.
</p>
<p>
  Maintenant que nous avons mis en lumière certains éléments sur lesquels ne
  porte pas ce site, revenons à son inspiration principale : la tradition et
  l’art de l’<a href="/fr/hermeneutics">herméneutique</a>.
</p>
