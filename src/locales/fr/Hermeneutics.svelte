<h2>SUR L'HERMÉNEUTIQUE</h2>
<p>
  L’herméneutique est impliquée dans l’étude et l’art de l’interprétation. Si
  les origines du terme « herméneutique » sont quelque peu disputées, elles sont
  parfois associées avec le rôle d’Hermès, le messager des dieux grecs,
  apportant des informations aux hommes.
</p>
<p>
  L’herméneutique a toujours été préoccupée, depuis ses origines et encore
  aujourd’hui, par le déchiffrage de textes difficiles et obscurs.
</p>
<p>
  Et donc, en premier lieu, l’herméneutique a été impliquée, par-dessus tout et
  quasiment exclusivement, avec des textes écrits, le plus souvent religieux ou
  légaux. Mais l’herméneute français Paul Ricœur a étendu, au vingtième siècle,
  le domaine de l’herméneutique aux créations humaines qui apparaissent
  « analogues aux textes ». Celles-ci comprennent par exemple l’architecture, la
  peinture et la plupart des expressions artistiques incluant, dans notre cas,
  les films et le cinéma. Maintenant, avançons plus loin dans la compréhension
  des films à travers l’herméneutique.
</p>
<p>
  Regardons, plus spécifiquement, comment le besoin de mieux comprendre des
  films difficiles et significatifs nous mène à l’herméneutique.
</p>
<p>
  Dans un certain sens, tous les films nous parlent de l’histoire humaine, mais,
  sur ce site, nous voulons nous concentrer sur des films qui nous apparaissent
  particulièrement significatifs à cet égard.
</p>
<p>
  Parmi ces nombreux films qui nous parlent d’une manière significative, nous
  souhaitons nous concentrer sur des films où une partie du contenu est
  présentée de manière quelque peu indirecte et a besoin d’être déchiffrée
  par-delà notre compréhension immédiate. L’histoire qui est racontée dans le
  film contient des aspects explicites alors qu’en même temps, certains aspects
  sont laissés implicites ou discrets. Ce sont ces aspects implicites et
  discrets qui forment le matériau de base de l’interprétation du <em
    >Movie Shrink</em
  >. Ils ne sont pas réellement « cachés » mais ils ont besoin d’être
  interprétés, ils ont besoin d’un travail additionnel de la part du spectateur.
  Telle est l’approche herméneutique à la compréhension de films significatifs.
</p>
<p>
  Ce travail additionnel requis de la part du spectateur ou de l’analyste
  revient souvent à trouver un fil conducteur qui n’est pas explicite dès le
  départ dans l’histoire que raconte le film. La compréhension additionnelle qui
  en résulte nous fait voir le film sous un autre jour, souvent en liant
  ensemble des éléments qui semblent, de prime abord, décousus. La tâche que
  nous souhaitons réaliser dans ce site d’analyse de film est alors de trouver
  une unité de sens dans certains films par-delà leur premier visionnement. Une
  fois ce fil conducteur dévoilé, il reste une autre tâche, celle de lier
  l’histoire du film à la société contemporaine et à notre histoire collective,
  telle qu’elle se développe au vingt et unième siècle. C’est ce que nous
  appellerons parfois « l’éléphant derrière le film ( ou derrière l’écran) ».
</p>

<h3>FONDEMENTS DU MOVIE SHRINK</h3>
<p>
  Tout en demeurant attentifs au risque d’aller trop loin dans notre désir de
  comprendre et d’interpréter, nous pensons néanmoins que les créations
  artistiques ont quelque chose à nous dire à propos de l’expérience humaine,
  par-delà leurs dimensions esthétiques, comme suggéré ailleurs. Le philosophe
  allemand Hans-Georg Gadamer, un spécialiste de l’herméneutique, a critiqué,
  dans sa longue et distinguée carrière, la tendance commune à considérer l’art
  comme uniquement une question d’esthétique. L’art, selon Gadamer, est
  également une source de connaissance à propos de la société, et plus
  spécifiquement à propos de l’expérience humaine contemporaine. Comme artiste,
  le réalisateur de films lui-même ne prévoit pas toujours présenter cette
  connaissance de manière délibérée, comme suggéré ailleurs. Ceci n’est pas un
  problème, tout au contraire, car il se trouve un certain degré d’inconscience
  dans toutes les créations artistiques. Le rôle du processus herméneutique est
  de porter à un plus haut niveau de conscience ce sens lorsque ce dernier est
  difficile à identifier.
</p>
<p>
  La méthode herméneutique est par ailleurs inutile lorsque, à un extrême d’un
  continuum, le sens est clair ou lorsque, à l’autre extrême, se trouve une
  surabondance de symboles et métaphores surchargées, partant dans toutes les
  directions et obscurcissant ainsi le contenu du film à l’étude.
</p>
<p>
  D’autres éléments fondamentaux inspirent notre site,
  <em>Le Movie Shrink</em>.
</p>
<p>
  Nous avons référé ailleurs à la contribution de l’herméneute français Paul
  Ricœur au domaine, et plus précisément à sa suggestion que nous devrions
  inclure comme objet de l’herméneutique des créations apparaissant « analogues
  aux textes », ce qui nous permet de considérer les films et le cinéma comme
  objets légitimes de l’interprétation herméneutique.
</p>
<p>
  Une autre contribution de Ricœur est également pertinente pour notre présent
  propos : c’est son idée que, lorsque nous interprétons un texte ou quelque
  chose d’analogue à un texte, nous ne débutons pas, pour ainsi dire, avec une
  page blanche, sans aucun préjugé.
</p>
<p>
  Parfois, et peut-être même la plupart du temps, nous possédons une idée de ce
  que nous tentons de trouver, une sorte de compréhension préliminaire de ce que
  nous cherchons. Ricœur nomme cette précompréhension, et cette idée préalable,
  l’herméneutique du « soupçon », qu’il oppose à l’herméneutique de
  l’«ouverture », lorsque nous débutons, dans ce dernier cas, par une complète
  ouverture d’esprit, dans un esprit totalement réceptif.
</p>
<p>
  L’herméneutique du soupçon, comme point de départ, n’est pas meilleure ou pire
  que l’herméneutique de l’ouverture : elles sont simplement différentes. La
  précompréhension de l’herméneutique du soupçon n’est pas nécessairement
  problématique puisque notre précompréhension peut être utile si elle est
  disciplinée et abandonnée lorsqu’elle se révèle fausse ou qu’elle nous égare.
  Plusieurs autres philosophes herméneutes ont d’ailleurs porté attention à ce
  processus de précompréhension, Gadamer et son maître, Heidegger, ayant
  remarqué qu’il était inévitable et devait être reconnu.
</p>
<p>
  Dans cet esprit, nous voulons rendre explicites certains éléments de base qui
  constituent notre propre précompréhension des films que nous étudions sur ce
  site.
</p>
<p>
  Premièrement, il doit être précisé que l’auteur de ce site provient de ce qui
  peut être appelé les humanités et les sciences sociales, plus spécifiquement
  de la science politique, de la sociologie et de l’économie. Dans notre
  précompréhension des films que nous choisissons d’étudier, nous croyons au
  départ qu’aucun film ne peut échapper au contexte socio-économique de sa
  création. Il ne peut que refléter la période qui lui a donné naissance, que le
  film se situe au passé, au présent ou dans le futur. Un film, en effet, ne
  peut que révéler quelque chose à propos du contexte socio-économique dans
  lequel il apparaît. À travers le vingtième-siècle, et au-delà,, l’histoire du
  cinéma, débutant en même temps que le vingtième siècle lui-même, a été témoin
  de toutes sortes de changements historiques, sociaux et économiques,
  reflétant, parmi d’autres changements, l’accroissement de la portée des médias
  et l’agrandissement (et peut-être aujourd’hui le rétrécissement) de la taille
  et de l’échelle des organisations humaines, des villages jusqu’au niveau
  global, en passant par la ville et le niveau national. Ces éléments se
  produisent à grande échelle et évoluent lentement, à une vitesse qui ressemble
  plus au mouvement d’un éléphant qu’à celle d’un rapide félin. Nous pouvons
  parfois en voir un aperçu à travers les œuvres d’art et les films.
</p>
<p>
  Lorsque nous étudions les films sur lesquels nous choisissons de passer du
  temps, qui ne sont bien entendu pas choisis au hasard, nous sommes conscients
  de ces dynamiques socio-économiques, non pour les juger ou prendre position à
  leur sujet, mais pour mieux les décrire et les comprendre.
</p>
<p>
  Idéalement, le récit que le film met en scène est relié à l’évolution de
  l’histoire socio-économique à laquelle nous participons, en tant qu’humains, à
  ce point dans notre développement. Les films peuvent être considérés en effet
  comme reflétant l’histoire humaine.
</p>
<p>
  En tentant de comprendre ces aspects socio-économiques et historiques des
  films à l’étude, nous comptons sur quelques disciplines spécifiques qui,
  prises ensemble, constituent les éléments de notre précompréhension et de
  notre herméneutique du soupçon à nous.
</p>
<p>
  Puisant dans la sociologie, nous sommes guidés par ses traditions
  interprétatives et historiques, que l’on retrouve, parmi d’autres, dans
  l’œuvre de
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Max_Weber">Max Weber</a
  >. Au niveau de l’économie, nous sommes principalement intéressés par l'
  <a
    target="_blank"
    href="https://fr.wikipedia.org/wiki/Institutional_economics"
    >institutionnalisme économique</a
  >
  (parfois appelé
  <a
    target="_blank"
    href="https://fr.wikipedia.org/wiki/New_institutional_economics"
    >nouvel institutionnalisme économique</a
  >) une école qui s’intéresse essentiellement aux changements apportés par
  l’évolution des modes d’interactions humaines et économiques. Finalement, du
  point de vue de l’histoire des médias, l’œuvre du philosophe des médias
  canadien
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Marshall_McLuhan"
    >Marshall McLuhan</a
  >
  (le « village global » et « le médium est le message ») constitue pour nous une
  importante source de compréhension.
</p>
<p>
  Ces éléments et auteurs peuvent être considérés comme constituant une part
  importante du matériau conceptuel de base du <em>Movie Shrink</em>
  enrichissant ainsi son approche herméneutique. Comme déclaré précédemment, reconnaître
  qu’il existe une sorte de précompréhension, qu’il est d’ailleurs possible d’abandonner
  au fil de l’interprétation, est en accord avec de nombreuses analyses herméneutiques.
  Nous n’avons fait ici qu’exposer quelques éléments de notre propre précompréhension
  en référant à
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Max_Weber">Max Weber</a
  >, à la
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Verstehen#Meaning"
    >sociologie interprétative</a
  >, à l'<a
    target="_blank"
    href="https://fr.wikipedia.org/wiki/Institutional_economics"
    >économie institutionnelle</a
  >, ainsi qu’aux théories de McLuhan à propos de l’histoire des médias.
</p>
<p>
  D’autres sources d’interprétation des films sont utiles pour nous, mais celles
  que l’on vient de mentionner ici peuvent être considérées comme les
  principales.
</p>

