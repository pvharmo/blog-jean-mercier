<h1>QUESTIONS-RÉPONSES</h1>
<p>
  Qu’elles proviennent d’amis, de collègues ou du web, voici une sélection de
  questions fréquemment posées (et leurs réponses) à propos de notre site
  d’interprétations de films.
</p>
<h3>
  Q. Comment <em>Le Movie Shrink</em> choisit-il les films qu’il commentera?
</h3>
<p>
  R. Bien qu’un grand soin soit pris pour choisir les films sur lesquels nous
  nous penchons, des éléments contingents entrent bien entendu en ligne de
  compte dans notre sélection. Nous visionnons plusieurs films à propos desquels
  nous choisissons éventuellement de ne pas commenter en détail. Certains de
  ceux-ci seront brièvement l’objet d’un commentaire dans notre section « <a
    href="/fr/category/bullit-comments">Commentaires-éclair</a
  > ».
</p>
<p>
  Les films que nous interprétons dans notre rubrique principale sont choisis
  parmi les films qui sont accessibles au Québec, sur le web, ou dans les
  festivals de films auxquels nous assistons. Nous ne prétendons pas que les
  films choisis sont les plus significatifs des films contemporains, mais ils en
  font certainement partie. Bien entendu, de nombreux films significatifs (et,
  en vérité, la majorité d’entre eux) ne seront pas interprétés par <em
    >Le Movie Shrink</em
  >. Après tout, beaucoup de films significatifs sont réalisés chaque année sur
  la planète, sur tous les continents.
</p>
<p>
  Il est pris pour acquis que si nous choisissons d’interpréter un film, c’est
  qu’il s’y trouve une dimension historique ou socio-économique, généralement
  implicite, que nous tentons de dévoiler et de comprendre. Mais le sens que
  nous tenterons de comprendre n’ira pas dans toutes les directions : il lui
  faudra présenter un fil conducteur et une certaine unité. Le plus souvent, le
  sens que nous tenterons de dévoiler sera lié aux éléments de compréhension que
  nous avons présentés dans notre section sur les fondements du <em
    >Movie Shrink</em
  >. Les sources qui y sont mentionnées peuvent être considérées comme les clefs
  nous permettant d’ouvrir la porte de l’interprétation des films, sur ce site.
  La
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Verstehen#Meaning"
    >sociologie interprétative</a
  >,
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Max_Weber">Max Weber</a
  >, l'<a
    target="_blank"
    href="https://fr.wikipedia.org/wiki/Institutional_economics"
    >économie institutionnelle</a
  >, et
  <a target="_blank" href="https://fr.wikipedia.org/wiki/Marshall_McLuhan"
    >Marshall McLuhan</a
  >
  sont, pour le dire ainsi, les ingrédients de base les plus importants permettant
  de réaliser les interprétations présentées ici.
</p>
<h3>
  Q. Comment <em>Le Movie Shrink</em> parvient-il à identifier ce qu’il considère
  comme les éléments significatifs d’un film donné?
</h3>
<p>
  R. Bien que nous tentions d’être aussi explicites et systématiques que
  possible, la méthode <a href="/fr/hermeneutics">herméneutique</a> que nous
  suivons ici ne se laisse pas vérifier et valider par les procédures standards
  de la méthode scientifique. Dès le départ, il nous faut reconnaître que les
  films ne sont pas choisis de manière aléatoire. Il se trouve dans la sélection
  une certaine part d’intuition et d’expérience. Cela étant dit, une fois que
  l’interprétation a été présentée, elle doit être défendue et justifiée de la
  manière la plus objective possible ; elle doit être à même de supporter
  critiques et opinions contraires, à l’image de ce qui se produit durant un
  procès.
</p>
<p>
  Le but principal est l’identification d’un thème central, apparaissant quelque
  peu indirectement et exprimé de diverses manières.
</p>
<p>
  Ce thème central est au cœur de ce que nous recherchons. Si ce thème central
  apparaît absent, nous n’avons pas besoin d’aller plus loin. Dans ces cas où
  nous ne parvenons pas à voir ce thème central implicite, lorsque les films que
  nous avons visionnés, bien qu’ils puissent être socialement importants, voire
  profonds, ne présentent pas d’éléments à dévoiler, la méthode
  <a href="/fr/hermeneutics">herméneutique</a> ne révélera pas plus que ce que le
  scénario nous apprend. Dans certains cas, le réalisateur du film est
  expressément conscient de ce qu’il souhaite exprimer, au point où ses films
  plaident ouvertement pour une cause donnée, souvent politique, à l’image des
  films de Costa-Gavras du vingtième siècle, qui visaient à révéler la
  corruption gouvernementale en Amérique du Sud et dans le reste du monde. La
  méthode
  <a href="/fr/hermeneutics">herméneutique</a> telle que nous la comprenons, ne peut
  rien ajouter de plus dans ces cas, comme dans un documentaire où l’opinion du réalisateur
  est primordiale et, en un sens, suffisante en elle-même.
</p>

<h3>Q. Existe-t-il autant d’interprétations qu’il existe de spectateurs?</h3>
<p>
  R. Selon la perspective adoptée ici, il y a place pour différentes
  interprétations d’un film et ces interprétations, bien que différentes, ne
  sont pas nécessairement en opposition les unes avec les autres. Dans l’espace
  interactif de notre site (<b>Le Forum du Movie Shrink</b>) se trouve un lieu
  de rencontre pour d’autres interprétations, d’autres éléments de
  compréhension.
</p>
<p>
  Ceci étant dit, toutes les interprétations ne sont pas équivalentes. Certaines
  interprétations sont, dans la vision de l’<a href="/fr/hermeneutics"
    >herméneutique</a
  >, « plus proches de l’objet » ou « plus sensibles à l’objet ». Certaines
  interprétations nous éclairent plus que d’autres. Nous ne sommes pas ici,
  comme suggéré précédemment, dans le cadre d’une preuve scientifique, mais ceci
  n’est pas à dire que tout est relatif. Il existe une nécessité de démontrer la
  pertinence de la compréhension et de l’interprétation proposées et, tel que
  dit précédemment, de les défendre, à l’image d’un procès, où la certitude
  absolue ne peut pas toujours être atteinte et où, pourtant, une décision est
  éventuellement prise.
</p>

